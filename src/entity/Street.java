package entity;

public class Street {
    private String number;
    private String name;


    // Getter Methods

    public String getNumber() {
        return number;
    }

    public String getName() {
        return name;
    }

    // Setter Methods

    public void setNumber( String number ) {
        this.number = number;
    }

    public void setName( String name ) {
        this.name = name;
    }
}