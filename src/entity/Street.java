package entity;

public class Street {
    private float number;
    private String name;


    // Getter Methods

    public float getNumber() {
        return number;
    }

    public String getName() {
        return name;
    }

    // Setter Methods

    public void setNumber( float number ) {
        this.number = number;
    }

    public void setName( String name ) {
        this.name = name;
    }
}