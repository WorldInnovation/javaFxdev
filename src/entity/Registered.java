package entity;

public class Registered {
    private String date;
    private float age;


    // Getter Methods

    public String getDate() {
        return date;
    }

    public float getAge() {
        return age;
    }

    // Setter Methods

    public void setDate( String date ) {
        this.date = date;
    }

    public void setAge( float age ) {
        this.age = age;
    }
}
